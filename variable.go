package main

import "fmt"

func main() {
	var x int = 5
	fmt.Println("Value of X is:", x)
	//Camel case variable
	var myVariable string = "Jhon"
	fmt.Println("myVariable is a camle caes variable who's value is:", myVariable)
	//Pascal Case variable
	var MyVariable int = 2222
	fmt.Println("MyVariable is a Pascal Case variable who's value is:", MyVariable)
	// Snake Case variable
	var my_variable_name = "Venom"
	fmt.Println("my_variable_name is a Snake Case variable who's value is:", my_variable_name)

}
